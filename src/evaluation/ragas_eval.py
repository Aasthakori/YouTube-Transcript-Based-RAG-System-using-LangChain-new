"""RAGAS-based evaluation of the RAG pipeline."""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any

import numpy as np

from langchain_core.output_parsers import StrOutputParser
from langchain_huggingface import HuggingFaceEmbeddings

from src.config import EMBEDDING_MODEL
from src.generation.chains import _get_llm
from src.generation.citations import format_with_sources, _REFUSAL_PHRASES
from src.generation.prompts import rag_prompt
from src.indexing.vector_store import load_index
from src.retrieval.hybrid import build_hybrid_retriever
from src.retrieval.reranker import build_reranking_retriever


def load_eval_dataset(path: str | Path) -> list[dict[str, Any]]:
    """Load evaluation samples from a JSON file.

    Each sample must contain:
        - ``question`` (str)
        - ``ground_truth`` (str)
        - ``answer`` (str) — generated by the RAG chain
        - ``contexts`` (list[str]) — retrieved chunk texts

    Args:
        path: Path to the JSON evaluation dataset file.

    Returns:
        List of evaluation sample dicts.

    Raises:
        FileNotFoundError: If the file does not exist.
        ValueError: If the file is empty or improperly structured.
    """
    path = Path(path)
    if not path.exists():
        raise FileNotFoundError(f"Eval dataset not found: {path}")
    with path.open() as f:
        data = json.load(f)
    if not isinstance(data, list) or not data:
        raise ValueError("Eval dataset must be a non-empty JSON array.")
    return data


def run_full_evaluation(
    dataset_path: str | Path = "evaluation/eval_dataset.json",
    answers_path: str | Path = "evaluation/results/answers_cache.json",
    results_path: str | Path = "evaluation/results/full_evaluation.json",
) -> dict[str, Any]:
    """Run the 2-layer evaluation system (no RAGAS, no LLM judge).

    Layer 1 — Retrieval metrics (deterministic):
        - Hit Rate @4: fraction of answerable questions where the correct
          video appears in the top-4 retrieved chunks.
        - MRR @4: Mean Reciprocal Rank — rewards finding the right video
          earlier in the ranked list.

    Layer 2 — Generation metrics (embeddings + string matching):
        - Answer Semantic Similarity: cosine similarity between system
          answer and ground-truth embeddings, averaged over answerable Qs.
        - Key Fact Coverage: fraction of ``key_facts`` substrings found
          in the system answer (case-insensitive).
        - Refusal Accuracy: fraction of unanswerable questions that trigger
          a refusal phrase.
        - Citation Coverage: answerable answers must contain ``[Source``;
          unanswerable answers must not.

    Answers and retrieved chunks are cached to ``answers_path`` before
    any metric is computed so the expensive chain calls are never repeated.

    Args:
        dataset_path: Path to eval dataset JSON (must have ``expected_video_id``
            and ``key_facts`` on answerable entries).
        answers_path: Where to write (or load) the answer cache.
        results_path: Where to write the final JSON report.

    Returns:
        Nested dict with Layer 1 and Layer 2 scores.
    """
    # --- 1. Load dataset ---
    rows = load_eval_dataset(dataset_path)
    print(f"Loaded {len(rows)} questions from {dataset_path}")

    answerable = [r for r in rows if r.get("ground_truth") != "NOT_IN_CONTEXT"]
    unanswerable = [r for r in rows if r.get("ground_truth") == "NOT_IN_CONTEXT"]
    print(f"  {len(answerable)} answerable  |  {len(unanswerable)} unanswerable")

    # Resolve results_path early so we can load the "before" baseline before overwriting.
    results_path = Path(results_path)
    before: dict[str, Any] | None = None
    if results_path.exists():
        with results_path.open() as f:
            before = json.load(f)

    # --- 2. Load from cache or run chain ---
    answers_path = Path(answers_path)
    cached_rows: list[dict[str, Any]] = []

    if answers_path.exists():
        with answers_path.open() as f:
            cached_rows = json.load(f)
        if len(cached_rows) == len(rows):
            print(f"Loading answers from cache: {answers_path}")

    if len(cached_rows) != len(rows):
        vector_store = load_index()
        all_docs = list(vector_store.docstore._dict.values())

        llm = _get_llm()
        parser = StrOutputParser()
        answer_chain = rag_prompt | llm | parser

        cached_rows = []
        for i, row in enumerate(rows, start=1):
            question: str = row["question"]
            print(f"  [{i}/{len(rows)}] {question[:80]}")

            ensemble = build_hybrid_retriever(vector_store, all_docs)
            retriever = build_reranking_retriever(ensemble)
            retrieved = retriever.invoke(question)
            top4 = retrieved[:4]

            # top-4 chunks with metadata for Layer 1 metrics
            retrieved_chunks = [
                {
                    "video_id": doc.metadata.get("video_id", ""),
                    "text": doc.page_content,
                }
                for doc in top4
            ]

            # use full retrieved list for generation context
            context_str, _ = format_with_sources(retrieved)
            answer: str = answer_chain.invoke(
                {
                    "context": context_str,
                    "question": question,
                    "chat_history": [],
                }
            )

            cached_rows.append(
                {
                    "question": question,
                    "answer": answer,
                    "ground_truth": row["ground_truth"],
                    "expected_video_id": row.get("expected_video_id"),
                    "key_facts": row.get("key_facts", []),
                    "retrieved_chunks": retrieved_chunks,
                }
            )

        answers_path.parent.mkdir(parents=True, exist_ok=True)
        with answers_path.open("w") as f:
            json.dump(cached_rows, f, indent=2)
        print(f"Answers saved to {answers_path}")

    # Split cached rows using the same logic
    cached_answerable = [r for r in cached_rows if r.get("ground_truth") != "NOT_IN_CONTEXT"]
    cached_unanswerable = [r for r in cached_rows if r.get("ground_truth") == "NOT_IN_CONTEXT"]

    # --- 3. Layer 1: Retrieval metrics ---

    # Metric 1: Hit Rate @4
    hits = [
        any(
            c["video_id"] == r["expected_video_id"]
            for c in r["retrieved_chunks"]
        )
        for r in cached_answerable
    ]
    hit_count = sum(hits)
    hit_rate = hit_count / len(cached_answerable) if cached_answerable else 0.0

    # Metric 2: MRR @4
    rr_list: list[float] = []
    for r in cached_answerable:
        rr = 0.0
        for rank, chunk in enumerate(r["retrieved_chunks"], start=1):
            if chunk["video_id"] == r["expected_video_id"]:
                rr = 1.0 / rank
                break
        rr_list.append(rr)
    mrr = sum(rr_list) / len(rr_list) if rr_list else 0.0

    # --- 4. Layer 2: Generation metrics ---

    # Metric 3: Answer Semantic Similarity
    embeddings = HuggingFaceEmbeddings(model_name=EMBEDDING_MODEL)
    sims: list[float] = []
    for r in cached_answerable:
        a_emb = np.array(embeddings.embed_query(r["answer"]))
        g_emb = np.array(embeddings.embed_query(r["ground_truth"]))
        norm = np.linalg.norm(a_emb) * np.linalg.norm(g_emb)
        sims.append(float(np.dot(a_emb, g_emb) / norm) if norm > 0 else 0.0)
    semantic_sim = sum(sims) / len(sims) if sims else 0.0

    # Metric 4: Key Fact Coverage
    facts_found = facts_total = 0
    for r in cached_answerable:
        for fact in r.get("key_facts", []):
            facts_total += 1
            if fact.lower() in r["answer"].lower():
                facts_found += 1
    key_fact_cov = facts_found / facts_total if facts_total else 0.0

    # Metric 5: Refusal Accuracy
    refused = [
        any(p in r["answer"].lower() for p in _REFUSAL_PHRASES)
        for r in cached_unanswerable
    ]
    refusal_count = sum(refused)
    refusal_acc = refusal_count / len(cached_unanswerable) if cached_unanswerable else 0.0

    # Metric 6: Citation Coverage
    def _citation_ok(r: dict[str, Any]) -> bool:
        has_cite = "[Source" in r["answer"]
        return (not has_cite) if r["ground_truth"] == "NOT_IN_CONTEXT" else has_cite

    cite_correct = sum(_citation_ok(r) for r in cached_rows)
    cite_cov = cite_correct / len(cached_rows)

    # Metric 7: Correct Answer Rate (answerable questions only)
    # Counts how many answerable questions received a substantive answer (not a refusal).
    substantive_count = sum(
        not any(p in r["answer"].lower() for p in _REFUSAL_PHRASES)
        for r in cached_answerable
    )
    correct_answer_rate = substantive_count / len(cached_answerable)

    # --- 5. Build result dict ---
    result: dict[str, Any] = {
        "layer1_retrieval": {
            "hit_rate_at_4": {
                "score": round(hit_rate, 4),
                "hits": hit_count,
                "total": len(cached_answerable),
            },
            "mrr_at_4": {
                "score": round(mrr, 4),
            },
        },
        "layer2_generation": {
            "semantic_similarity": {
                "score": round(semantic_sim, 4),
            },
            "key_fact_coverage": {
                "score": round(key_fact_cov, 4),
                "found": facts_found,
                "total": facts_total,
            },
            "refusal_accuracy": {
                "score": round(refusal_acc, 4),
                "correct": refusal_count,
                "total": len(cached_unanswerable),
            },
            "citation_coverage": {
                "score": round(cite_cov, 4),
                "correct": cite_correct,
                "total": len(cached_rows),
            },
            "correct_answer_rate": {
                "score": round(correct_answer_rate, 4),
                "correct": substantive_count,
                "total": len(cached_answerable),
            },
        },
    }

    # --- 6. Save results ---
    results_path.parent.mkdir(parents=True, exist_ok=True)
    with results_path.open("w") as f:
        json.dump(result, f, indent=2)
    print(f"\nResults saved to {results_path}")

    # --- 7. Print report ---
    w = 60
    print("\n" + "=" * w)
    print("LAYER 1 — RETRIEVAL METRICS")
    print("-" * w)
    print(f"{'Hit Rate @4':<35} {hit_rate:>6.4f}   {hit_count}/{len(cached_answerable)} questions")
    print(f"{'Mean Reciprocal Rank (MRR @4)':<35} {mrr:>6.4f}")
    print("-" * w)
    print("LAYER 2 — GENERATION METRICS")
    print("-" * w)
    print(f"{'Answer Semantic Similarity':<35} {semantic_sim:>6.4f}   avg over {len(cached_answerable)} answerable")
    print(f"{'Key Fact Coverage':<35} {key_fact_cov:>6.4f}   {facts_found}/{facts_total} facts found")
    print(f"{'Refusal Accuracy':<35} {refusal_acc:>6.4f}   {refusal_count}/{len(cached_unanswerable)} unanswerable refused")
    print(f"{'Citation Coverage':<35} {cite_cov:>6.4f}   {cite_correct}/{len(cached_rows)} correct")
    print(f"{'Correct Answer Rate':<35} {correct_answer_rate:>6.4f}   {substantive_count}/{len(cached_answerable)} gave substantive answers")
    print("=" * w)

    if before:
        print("\nBEFORE / AFTER COMPARISON")
        print("-" * w)
        print(f"{'Metric':<35} {'Before':>8}  {'After':>8}  {'Delta':>8}")
        print("-" * w)
        comparisons = [
            ("Hit Rate @4",          before["layer1_retrieval"]["hit_rate_at_4"]["score"],         round(hit_rate, 4)),
            ("MRR @4",               before["layer1_retrieval"]["mrr_at_4"]["score"],              round(mrr, 4)),
            ("Answer Semantic Sim",  before["layer2_generation"]["semantic_similarity"]["score"],   round(semantic_sim, 4)),
            ("Key Fact Coverage",    before["layer2_generation"]["key_fact_coverage"]["score"],     round(key_fact_cov, 4)),
            ("Refusal Accuracy",     before["layer2_generation"]["refusal_accuracy"]["score"],      round(refusal_acc, 4)),
            ("Citation Coverage",    before["layer2_generation"]["citation_coverage"]["score"],     round(cite_cov, 4)),
            ("Correct Answer Rate",  before["layer2_generation"]["correct_answer_rate"]["score"],   round(correct_answer_rate, 4)),
        ]
        for name, b, a in comparisons:
            delta = a - b
            sign = "+" if delta >= 0 else ""
            print(f"{name:<35} {b:>8.4f}  {a:>8.4f}  {sign}{delta:>7.4f}")
        print("=" * w)

    return result


if __name__ == "__main__":
    run_full_evaluation()
